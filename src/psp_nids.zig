/// Resolve a PSP NID to its function name. O(log N) binary search.
pub fn resolveNid(nid: u32) ?[]const u8 {
    var lo: usize = 0;
    var hi: usize = nid_table.len;
    while (lo < hi) {
        const mid = lo + (hi - lo) / 2;
        if (nid_table[mid].nid == nid) return nid_table[mid].name;
        if (nid_table[mid].nid < nid) {
            lo = mid + 1;
        } else {
            hi = mid;
        }
    }
    return null;
}

